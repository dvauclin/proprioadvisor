// Responsive dialog content that automatically chooses the right component
const ResponsiveDialogContent = React.forwardRef<
  React.ElementRef<typeof DialogPrimitive.Content>,
  React.ComponentPropsWithoutRef<typeof DialogPrimitive.Content> & { forceMobile?: boolean }
>(({ forceMobile, className, children, ...props }, ref) => {
  const [isMobile, setIsMobile] = React.useState(false);
  const [hasMounted, setHasMounted] = React.useState(false);

  React.useEffect(() => {
    setHasMounted(true);
    const checkMobile = () => {
      setIsMobile(window.innerWidth < 768);
    };
    
    checkMobile();
    window.addEventListener('resize', checkMobile);
    return () => window.removeEventListener('resize', checkMobile);
  }, []);

  if (!hasMounted) {
    return <DialogContent ref={ref} className={className} {...props}>{children}</DialogContent>;
  }

  const shouldUseMobile = forceMobile || isMobile;
  const Component = shouldUseMobile ? DialogContentMobile : DialogContent;

  return <Component ref={ref} className={className} {...props}>{children}</Component>;
});
ResponsiveDialogContent.displayName = "ResponsiveDialogContent";

